export const getNotificationForDay = (currentDay) => {
  if (currentDay < 1) return notificationsData.filter((n) => n.day === 0);

  let activeNotifs = [];

  for (let i = 1; i <= currentDay; i++) {
    const dailyNotifs = notificationsData.filter((n) => n.day === i);
    if (dailyNotifs.length > 0) {
      activeNotifs.push(...dailyNotifs);
    } else {
      activeNotifs.push({
        id: `generic_${i}`,
        day: i,
        title: `Puasa Hari ke-${i} ðŸŒ™`,
        message: `Terus semangat puasanya! Jangan lupa catat progres ibadahmu di Daily Tracker hari ini.`,
        type: 'info',
      });
    }
  }

  // Urutkan dari notifikasi hari terbaru di urutan paling atas
  return activeNotifs.sort((a, b) => b.day - a.day);
};
